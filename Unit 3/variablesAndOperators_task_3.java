public class example {
    /**
     * @param args
     */
    public static void main(String[] args) {
        // various operations
        int a = 15;
        int b = 5;
        int c = a - b;
        System.out.println("Subraction: " + c);
        c = a * b;
        System.out.println("Multiply: " + c);
        c = a / b;
        System.out.println("Divide: " + c);
        c = a % b;
        System.out.println("Percentage: " + c);
        c = a++;
        System.out.println("Positively increment by one: " + c);
        c = b--;
        System.out.println("Negatively increment by one: " + c);

    }

}